#include <stdio.h>

int	ft_str_is_numeric(char *str)
{
	int	a;
	int	b;

	a = 0;
	b = 0;
	if (str[a] == '\0')
		return (1);
	while (str[a] != '\0')
	{
		if (str[a] >= 48 && str[a] <= 57)
			b++;
		a++;
	}
	if (b == a)
		return (1);
	return (0);
}
