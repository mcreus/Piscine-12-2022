#include <stdio.h>

int	ft_str_is_printable(char *src)
{
	int	a;
	int	b;

	a = 0;
	b = 0;
	if (src[a] == '\0')
		return (1);
	while (src[a] != '\0')
	{
		if (src[a] >= 32 && src[a] <= 126)
		{
			b++;
		}
		a++;
	}
	if (b == a)
		return (1);
	return (0);
}
